def mess_with_vars(one, two, three) # reassignment so doesn't change original variable when passed into method
  one = two
  two = three
  three = one
end

one = "one"
two = "two"
three = "three"

mess_with_vars(one, two, three)

puts "one is: #{one}" 
puts "two is: #{two}"
puts "three is: #{three}"

